def generate_css(config: dict) -> str:
    """Generate complete CSS custom properties."""
    lines = []
    lines.append("/* ==========================================================")
    lines.append("   Design System Tokens")
    lines.append("   Generated by frontend-design skill")
    lines.append("   ========================================================== */")
    lines.append("")
    lines.append(":root {")

    # Colors
    lines.append("  /* --- Colors --- */")
    lines.append(f"  --color-primary: hsl({config['primary']});")
    lines.append(f"  --color-secondary: hsl({config['secondary']});")
    lines.append(f"  --color-accent: hsl({config['accent']});")
    lines.append(f"  --color-bg: hsl({config['bg']});")
    lines.append(f"  --color-fg: hsl({config['fg']});")
    lines.append(f"  --color-primary-hsl: {config['primary']};")
    lines.append(f"  --color-bg-hsl: {config['bg']};")
    lines.append(f"  --color-fg-hsl: {config['fg']};")

    # Surface colors
    if config["theme"] == "dark":
        lines.append("  --color-surface: hsl(220, 12%, 12%);")
        lines.append("  --color-surface-hover: hsl(220, 12%, 16%);")
        lines.append("  --color-border: hsl(220, 10%, 18%);")
        lines.append("  --color-muted: hsl(220, 10%, 40%);")
    else:
        lines.append("  --color-surface: hsl(0, 0%, 100%);")
        lines.append("  --color-surface-hover: hsl(0, 0%, 97%);")
        lines.append("  --color-border: hsl(0, 0%, 88%);")
        lines.append("  --color-muted: hsl(0, 0%, 55%);")

    # Semantic colors
    lines.append("  --color-success: hsl(142, 70%, 45%);")
    lines.append("  --color-warning: hsl(38, 92%, 50%);")
    lines.append("  --color-error: hsl(0, 72%, 51%);")
    lines.append("  --color-info: hsl(210, 92%, 55%);")
    lines.append("")

    # Typography
    lines.append("  /* --- Typography --- */")
    lines.append(f"  --font-display: '{config['font_display']}', sans-serif;")
    lines.append(f"  --font-body: '{config['font_body']}', sans-serif;")
    lines.append(f"  --font-mono: '{config['font_mono']}', monospace;")
    lines.append("")

    # Type scale
    lines.append("  /* --- Type Scale (Major Third 1.250) --- */")
    for name, value in TYPE_SCALE.items():
        lines.append(f"  --text-{name}: {value};")
    lines.append("")

    # Line heights
    lines.append("  /* --- Line Heights --- */")
    lines.append("  --leading-tight: 1.1;")
    lines.append("  --leading-snug: 1.25;")
    lines.append("  --leading-normal: 1.5;")
    lines.append("  --leading-relaxed: 1.65;")
    lines.append("  --leading-loose: 1.8;")
    lines.append("")

    # Letter spacing
    lines.append("  /* --- Letter Spacing --- */")
    lines.append("  --tracking-tight: -0.03em;")
    lines.append("  --tracking-normal: 0;")
    lines.append("  --tracking-wide: 0.05em;")
    lines.append("  --tracking-wider: 0.08em;")
    lines.append("")

    # Spacing
    lines.append("  /* --- Spacing (8px Grid) --- */")
    for name, value in SPACING.items():
        lines.append(f"  --space-{name}: {value};")
    lines.append("")

    # Border radius
    radius = int(config.get("radius_base", 8))
    lines.append("  /* --- Border Radius --- */")
    lines.append(f"  --radius-sm: {max(radius // 2, 2)}px;")
    lines.append(f"  --radius-md: {radius}px;")
    lines.append(f"  --radius-lg: {radius + 4}px;")
    lines.append(f"  --radius-xl: {radius + 8}px;")
    lines.append(f"  --radius-2xl: {radius + 12}px;")
    lines.append("  --radius-full: 999px;")
    lines.append("")

    # Sizing
    lines.append("  /* --- Component Sizing --- */")
    lines.append("  --size-btn-sm: 32px;")
    lines.append("  --size-btn-md: 40px;")
    lines.append("  --size-btn-lg: 48px;")
    lines.append("  --size-btn-xl: 56px;")
    lines.append("  --size-input: 40px;")
    lines.append("  --size-nav: 64px;")
