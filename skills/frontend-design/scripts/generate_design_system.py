    # Content widths
    lines.append("  /* --- Content Widths --- */")
    lines.append("  --width-prose: 65ch;")
    lines.append("  --width-content: 720px;")
    lines.append("  --width-page: 1200px;")
    lines.append("  --width-wide: 1400px;")
    lines.append("")

    # Animation
    lines.append("  /* --- Animation --- */")
    lines.append("  --duration-instant: 100ms;")
    lines.append("  --duration-fast: 200ms;")
